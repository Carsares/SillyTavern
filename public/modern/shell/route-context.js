export function createRouteContext({
    state,
    activity,
    api,
    assets,
    characters,
    chatContext,
    chatFiles,
    chatGeneration,
    chatMessages,
    components,
    constants,
    extensions,
    groups,
    personas,
    presets,
    settings,
    shell,
    utils,
    worldbooks,
}) {
    const {
        metricCard,
        pageHead,
        renderCharacterRow,
        renderEmptyState,
        renderGroupRow,
        renderInlineEmpty,
        renderKeyValue,
    } = components;

    return {
        state,
        ...utils,
        metricCard,
        pageHead,
        renderCharacterRow,
        renderEmptyState,
        renderGroupRow,
        renderInlineEmpty,
        renderKeyValue,
        ...shell,
        ...activity,
        ...constants,
        ...api,
        ...assets,
        ...characters,
        ...chatContext,
        ...chatFiles,
        ...chatGeneration,
        ...chatMessages,
        ...extensions,
        ...groups,
        ...personas,
        ...presets,
        ...settings,
        ...worldbooks,
    };
}
